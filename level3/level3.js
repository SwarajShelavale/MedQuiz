const container = document.getElementById('videoContainer');
const popup = document.getElementById("popup");
const popupText = document.getElementById("popupText");
const closeBtn = document.querySelector(".close-btn");
const mainContent = document.getElementById("mainContent");  // The content to blur

for (let i = 0; i < videoUrls.length; i++) {
    const videoElement = document.createElement('video');
    videoElement.controls = true;
    videoElement.src = videoUrls[i];

    const figureElement = document.createElement('figure');
    const figcaptionElement = document.createElement('figcaption');

    // Create a button to show the explanation popup
    const showPopupBtn = document.createElement('button');
    showPopupBtn.textContent = "Show Explanation";
    showPopupBtn.classList.add("show-popup-btn");

    // Add click event to show the popup with the correct explanation
    showPopupBtn.addEventListener('click', function() {
        popupText.textContent = explanations[i];
        popup.style.display = "block";
        mainContent.classList.add('blur-background');  // Add blur class to main content
    });

    figureElement.appendChild(videoElement);
    figureElement.appendChild(figcaptionElement);
    figureElement.appendChild(showPopupBtn);

    container.appendChild(figureElement);
}

// Close the popup when the close button is clicked
closeBtn.onclick = function() {
    popup.style.display = "none";
    mainContent.classList.remove('blur-background');  // Remove blur class from main content
};

// Close the popup when clicking outside of the popup window
window.onclick = function(event) {
    if (event.target === popup) {
        popup.style.display = "none";
        mainContent.classList.remove('blur-background');  // Remove blur class from main content
    }
};